    def ocr(self):
        pass
    
    @property
    def ocr(cls):
        pass

    @abstractmethod
    def __init__():
        pass

    @abstractmethod
    def setCls(cls):
        pass

    @abstractmethod
    def getTextFromImage(img):
        pass

